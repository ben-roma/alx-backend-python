#!/usr/bin/env python3
"""This module defines a type-annotated function to_str that takes a float n
as argument and returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """
    Converts a float to its string representation.

    Args:
        n (float): The float number.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
